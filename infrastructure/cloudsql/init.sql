-- Pavilion-AI Agentic CMS Database Initialization Script
-- Run this after creating Cloud SQL instance

-- Enable required extensions
CREATE EXTENSION IF NOT EXISTS "uuid-ossp";
CREATE EXTENSION IF NOT EXISTS "pgcrypto";

-- Grant permissions to pavilion_user
GRANT CONNECT ON DATABASE pavilion_agentic TO pavilion_user;
GRANT USAGE ON SCHEMA public TO pavilion_user;
GRANT CREATE ON SCHEMA public TO pavilion_user;

-- Tables for content embeddings (Phase 1)
CREATE TABLE IF NOT EXISTS cms_contentembedding (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    article_id INTEGER NOT NULL,
    embedding_vector BYTEA NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    UNIQUE(article_id)
);

CREATE INDEX idx_contentembedding_article_id ON cms_contentembedding(article_id);
CREATE INDEX idx_contentembedding_created_at ON cms_contentembedding(created_at);

-- Table for tracking semantic searches
CREATE TABLE IF NOT EXISTS cms_semanticsearch (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    query TEXT NOT NULL,
    results_count INTEGER,
    execution_time_ms FLOAT,
    user_id INTEGER,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX idx_semanticsearch_created_at ON cms_semanticsearch(created_at);
CREATE INDEX idx_semanticsearch_user_id ON cms_semanticsearch(user_id);

