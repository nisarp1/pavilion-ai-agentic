-- Tables for Analytics (Phase 4)
CREATE TABLE IF NOT EXISTS analytics_contentperformance (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    content_id INTEGER NOT NULL,
    content_version_id INTEGER,
    metric_date DATE NOT NULL,
    pageviews INTEGER DEFAULT 0,
    sessions INTEGER DEFAULT 0,
    user_engagement_duration_seconds FLOAT DEFAULT 0,
    bounce_rate FLOAT DEFAULT 0,
    avg_session_duration_seconds FLOAT DEFAULT 0,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    UNIQUE(content_id, content_version_id, metric_date)
);

CREATE INDEX idx_contentperformance_content_id ON analytics_contentperformance(content_id);
CREATE INDEX idx_contentperformance_metric_date ON analytics_contentperformance(metric_date);

-- Tables for Agent Workflows (Phase 3 - HITL)
CREATE TABLE IF NOT EXISTS cms_agentworkflow (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    article_id INTEGER NOT NULL,
    workflow_type VARCHAR(50) NOT NULL,
    status VARCHAR(50) DEFAULT 'pending',
    state JSONB DEFAULT '{}',
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    completed_at TIMESTAMP WITH TIME ZONE
);

CREATE INDEX idx_agentworkflow_article_id ON cms_agentworkflow(article_id);
CREATE INDEX idx_agentworkflow_status ON cms_agentworkflow(status);
CREATE INDEX idx_agentworkflow_created_at ON cms_agentworkflow(created_at);

-- Grants for all tables
GRANT SELECT, INSERT, UPDATE, DELETE ON ALL TABLES IN SCHEMA public TO pavilion_user;
GRANT USAGE, SELECT ON ALL SEQUENCES IN SCHEMA public TO pavilion_user;
GRANT EXECUTE ON ALL FUNCTIONS IN SCHEMA public TO pavilion_user;

-- Audit table
CREATE TABLE IF NOT EXISTS cms_auditlog (
    id BIGSERIAL PRIMARY KEY,
    workflow_id UUID,
    event_type VARCHAR(100) NOT NULL,
    event_details JSONB,
    user_id INTEGER,
    timestamp TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX idx_auditlog_workflow_id ON cms_auditlog(workflow_id);
CREATE INDEX idx_auditlog_timestamp ON cms_auditlog(timestamp);
CREATE INDEX idx_auditlog_event_type ON cms_auditlog(event_type);

-- Grant audit table permissions
GRANT SELECT, INSERT ON cms_auditlog TO pavilion_user;

-- Constraints and comments
COMMENT ON TABLE cms_contentembedding IS 'Stores vector embeddings for articles generated by Vertex AI';
COMMENT ON TABLE cms_semanticsearch IS 'Logs semantic search queries for analytics and optimization';
COMMENT ON TABLE analytics_contentperformance IS 'Performance metrics for article versions from GA4';
COMMENT ON TABLE cms_agentworkflow IS 'Tracks AI agent workflow execution for articles';
COMMENT ON TABLE cms_auditlog IS 'Complete audit trail of all actions';

